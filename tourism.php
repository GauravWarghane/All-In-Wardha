        <p style="text-align: justify; text-indent: 20px;" id="place1txt">  Sevagram is a small village near Wardha town (8 km). Mahatma Gandhi reached the village on the 30th April,1936 early in the morning at about 5 a.m. He remained here for about five to six days. He called the villagers and the purpose of his visit and sought their permission to settle down here. He had no intention to keep anybody with him except his wife Kasturba. However slowly pressure of work necessitated permission to more colleagues till Sevagram Ashram also became a full-fledged institution.
    
